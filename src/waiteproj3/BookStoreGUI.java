    private void AddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewActionPerformed

        //check for errors
        //verify selection
        try{
            if(bookRB.isSelected()){
                x = new Book();
                ((Book)x).setGenre(genreTF.getText());     
            }
            else if(movieRB.isSelected()){
                x = new Movie();
                ((Movie)x).setDirector(directorTF.getText());
                ((Movie)x).setActor(actorTF.getText());
            }
            else if(paintingRB.isSelected()){
                x = new Painting();
                ((Painting)x).setHeight(Integer.parseInt(heightTF.getText()));
                ((Painting)x).setWidth(Integer.parseInt(widthTF.getText()));
                ((Painting)x).setMedia(mediaTF.getText());
            }
            else
            throw new Exception();
        }
        catch(Exception ex){
            errorMessage.setText("ERROR: No category selected!");
        }
        //verify dates
        try{
            if (rDatePicker.getDate().after(aDatePicker.getDate()))
                throw new Exception();
            if (rDatePicker.getDate().after(Date.from(Instant.now())))
                throw new Exception();
            if (aDatePicker.getDate().after(Date.from(Instant.now())))
                throw new Exception();
        }
        catch(Exception ex){
            errorMessage.setText("ERROR: Invalid Dates!");
        }
        //verify text input
        try{
            if(genreTF.isEnabled()&& genreTF.getText().trim().isEmpty())
                throw new Exception();
            if(directorTF.isEnabled()&& directorTF.getText().trim().isEmpty())
                throw new Exception();
            if(actorTF.isEnabled()&& actorTF.getText().trim().isEmpty())
                throw new Exception();
            if(mediaTF.isEnabled()&& mediaTF.getText().trim().isEmpty())
                throw new Exception();
            if(heightTF.isEnabled()&& heightTF.getText().trim().isEmpty())
                throw new Exception();
            if(widthTF.isEnabled()&& widthTF.getText().trim().isEmpty())
                throw new Exception();
            if(aPriceTF.getText().trim().isEmpty())
                throw new Exception();
            if(titleTF.getText().trim().isEmpty())
                throw new Exception();
            if(pPriceTF.getText().trim().isEmpty())
                throw new Exception();
        }
        catch(Exception ex)
        {
            errorMessage.setText("ERROR: Please fill in all required fields");
        }
        inv[i]=x;
        setFields();
    }//GEN-LAST:event_AddNewActionPerformed

