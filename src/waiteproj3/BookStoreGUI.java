    private void RemoveBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveBActionPerformed
        try{
            //check for empty selection
            if(InvIDTF.getText().trim().isEmpty())
                throw new Exception();
            
            int e = Integer.parseInt(InvIDTF.getText());
            
            //check for invalid inventory selection
            if(e<0 || e>= inv.length)
                throw new Exception();
            
            //check for possible negative inventory attempt
            if(Integer.parseInt(QuantityTF.getText())> inv[e].getItemCount())
                throw new Exception();
            
            inv[e].remove(Integer.parseInt(QuantityTF.getText()));
            
        }
        catch(Exception ex){
            RemoveError.setText("ERROR! Invalid Operation.  Please Try again!");
        }
    }//GEN-LAST:event_RemoveBActionPerformed

